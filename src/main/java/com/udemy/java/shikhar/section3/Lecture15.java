package com.udemy.java.shikhar.section3;

public class Lecture15 {
    public static void main(String[]args){
        // addition operator or +
        // + does addition
        int myresult=12+8; // 12+6=18
        System.out.println("12+6= "+myresult);
        int previousresult=myresult;
        System.out.println("the previous result is "+previousresult);
        int newresult= myresult-1;
        System.out.println("the new result is 12+5-1= "+ newresult);

        //multiplication operator or *
        // * does multiplication
        myresult=myresult*2;
        System.out.println("18*2= "+myresult);

        //division operator or /
        myresult=12+8;
        // / does division
        myresult=myresult/5;
        System.out.println("20/5= "+myresult);

        //the remainder/modular operator or %
        // % calculates/returns the remainder of a operand dived by another operand
        myresult=17%3;
        System.out.println("the remainder of 17/3= "+myresult);
    }
}
